#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	size_src;

	i = 0;
	j = 0;
	size_src = ft_strlen(src);
	while (dest[i] != '\0' && i < size)
	{
		i++;
	}
	while (src[j] && (i + j + 1) < size)
	{
		dest[i + j] = src[j];
		j++;
	}
	if (i < size)
		dest[i + j] = '\0';
	return (i + size_src);
}
